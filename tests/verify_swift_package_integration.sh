ROOT_DIR="$(cd "$(dirname "$0")/.." && pwd)"

assert_file() {
    [[ -f "$ROOT_DIR/$1" ]] || { echo "missing file: $1" >&2; exit 1; }
}

assert_dir() {
    [[ -d "$ROOT_DIR/$1" ]] || { echo "missing directory: $1" >&2; exit 1; }
}

assert_missing() {
    [[ ! -e "$ROOT_DIR/$1" ]] || { echo "unexpected path: $1" >&2; exit 1; }
}

assert_contains() {
    grep -Fq -- "$2" "$ROOT_DIR/$1" || { echo "missing pattern '$2' in $1" >&2; exit 1; }
}

assert_not_contains() {
    grep -Fq -- "$2" "$ROOT_DIR/$1" && { echo "unexpected pattern '$2' in $1" >&2; exit 1; } || true
